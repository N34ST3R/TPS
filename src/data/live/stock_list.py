async def fetch_all_stocks() -> dict:
    now = time.time()
    cache_key = "all_stocks"
    if cache_key in _cache and now - _cache[cache_key]["_ts"] < _cache_ttl.get(
        cache_key, 300
    ):
        return {k: v for k, v in _cache[cache_key].items() if k != "_ts"}

    all_stocks = []

    for stock_list in STOCK_SECTORS.values():
        for symbol in stock_list:
            if not any(s["symbol"] == symbol for s in all_stocks):
                sector = next(
                    s for s, stocks in STOCK_SECTORS.items() if symbol in stocks
                )
                all_stocks.append(
                    {"symbol": symbol, "name": "", "sector": sector, "type": "stock"}
                )

    for item in INDICES:
        all_stocks.append({**item, "type": "index"})

    for item in CRYPTOS:
        all_stocks.append({**item, "type": "crypto"})

    for item in ETF_FOREX:
        all_stocks.append({**item, "type": "etf_forex"})

    result = {
        "stocks": all_stocks,
        "total": len(all_stocks),
        "sectors": list(STOCK_SECTORS.keys()),
        "updated_at": now,
    }
    result["_ts"] = now
    _cache[cache_key] = result
    _cache_ttl[cache_key] = 300

    return {k: v for k, v in result.items() if k != "_ts"}
